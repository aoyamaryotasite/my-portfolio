    function filterByAlphabet(filter) {
      var listItems = document.querySelectorAll('.list-soba-custom .list-item');
      var tabItems = document.querySelectorAll('.razize-name-link-soba .tab-item');

      // Loại bỏ class 'current' và 'no-data' từ tất cả các tab
      tabItems.forEach(function(tab) {
        tab.classList.remove('current');
      });

      // Thêm class 'current' vào tab được chọn
      event.currentTarget.classList.add('current');

      // Lọc và hiển thị dữ liệu
      listItems.forEach(function(item) {
        var itemText = item.textContent.trim();
        var isVisible = false;

        if (filter === 'all') {
          isVisible = true;
        } else if (filter === 'car-alphabet') {
          isVisible = /^[ａ-ｚＡ-Ｚ０-９]/.test(itemText);
        } else if (Array.isArray(filter)) {
          isVisible = filter.some(function(char) {
            return itemText.startsWith(char);
          });
        } else {
          isVisible = itemText.startsWith(filter);
        }

        if (isVisible) {
          item.style.display = 'flex';
        } else {
          item.style.display = 'none';
        }
      });
    }
