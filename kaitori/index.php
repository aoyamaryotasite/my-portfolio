    document.addEventListener('DOMContentLoaded', function() {

      function parseFilter(filter) {
        try {
          // Nếu là một chuỗi JSON, chuyển đổi thành mảng
          return JSON.parse(filter);
        } catch (e) {
          // Nếu không thành công, xem xét như là một giá trị đơn
          return [filter];
        }
      }


      function processDataAndDisplay() {
        var listItems = document.querySelectorAll('.list-soba-custom .list-item');
        var tabItems = document.querySelectorAll('.razize-name-link-soba .tab-item');

        if (listItems.length === 0 || tabItems.length === 0) {
          console.log('Không tìm thấy dữ liệu hoặc tab.');
          return;
        }

        tabItems.forEach(function(tab) {
          var filter = parseFilter(tab.getAttribute('data-filter'));
          var filteredItems = filterData(listItems, filter);

          checkTabData(tab, filteredItems);
        });
      }

      function checkTabData(tab, data) {
        if (!data || data.length === 0) {
          tab.classList.add('no-data');
          console.log('Tab ' + tab.getAttribute('data-filter') + ': Không có dữ liệu.');
        } else {
          tab.classList.remove('no-data');
          console.log('Tab ' + tab.getAttribute('data-filter') + ': Có dữ liệu.');
          console.log(data);
        }
      }

      function filterData(items, filter) {
        return Array.from(items).filter(function(item) {
          var itemText = item.textContent.trim();
          var firstChar = itemText.charAt(0);

          // Thêm log để kiểm tra giá trị filter
          console.log('Filter Value:', filter);

          // Kiểm tra nếu filter là 'all' hoặc không phải là mảng, trả về true
          if (filter.includes('all')) {
            return true;
          } else if (Array.isArray(filter) && filter.includes('car-alphabet')) {
            // Kiểm tra giá trị cho tab "car-alphabet" bằng cách so sánh chữ cái đầu tiên
            return /^[ａ-ｚＡ-Ｚ０-９]/.test(firstChar);
          } else if (Array.isArray(filter)) {
            // Kiểm tra nếu chữ cái đầu tiên thuộc một trong các giá trị trong filter
            return filter.includes(firstChar);
          } else {
            // Kiểm tra nếu chữ cái đầu tiên trùng với giá trị trong filter
            return firstChar === filter;
          }
        });
      }

      processDataAndDisplay();

    });
  </script>

